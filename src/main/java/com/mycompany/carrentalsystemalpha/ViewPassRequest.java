    private void loadPasswordR() {
  String sql = "SELECT u.user_id, u.full_name, u.Phone_num " +
             "FROM user_table u " +
             "JOIN user_data m ON u.user_id = m.user_id " +
             "WHERE m.reset_requested = TRUE";


    try (Connection conn = DatabaseConnection.connect();
         PreparedStatement pst = conn.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {

        DefaultTableModel model = (DefaultTableModel) tblrequest.getModel();
        model.setRowCount(0); // clear table first

        while (rs.next()) {
            int id = rs.getInt("user_id");
            String name = rs.getString("full_name");
            String phone = rs.getString("Phone_num");

            // add row to JTable
            model.addRow(new Object[]{id, name, phone});
            tblrequest.setDefaultEditor(Object.class, null);
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to load reset requests.");
    }
}

    
  private void selector() {
       tblrequest.getSelectionModel().addListSelectionListener(e -> {
    if (!e.getValueIsAdjusting() && tblrequest.getSelectedRow() != -1) {
        int selectedRow = tblrequest.getSelectedRow();
        buttons();
        
           
        String userId = tblrequest.getValueAt(selectedRow, 0).toString(); 
        this.userid = userId;
    }
    
    else {dbuttons();}
    });
   }       

