private void reject(String userId) {
    String sql = "UPDATE user_data SET reset_requested = 0 WHERE user_ID = ?";
    
    try (Connection conn = DatabaseConnection.connect();
         PreparedStatement pst = conn.prepareStatement(sql)) {
        
        pst.setString(1, userId);
        int rowsAffected = pst.executeUpdate(); // ✅ use executeUpdate
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "User Password Request has been Rejected.");
        }       
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
} 
 
  private void approve(String userId) {
        
    String sql = "UPDATE user_data SET reset_requested = 0, reset_confirmed = 1 where user_ID = ?";
    try (Connection conn = DatabaseConnection.connect();
         PreparedStatement pst = conn.prepareStatement(sql)) {
        pst.setString(1, userId);
        int rowsAffected = pst.executeUpdate(); // ✅ use executeUpdate
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "User Password Request has been Approved.");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
 }  
    

