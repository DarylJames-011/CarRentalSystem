    private void LogBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogBttnActionPerformed
      
  String username = UserF.getText();
  String password = new String(PassF.getPassword());

if (username.isEmpty() || password.isEmpty()) {
    Error dialog = new Error(this, true, "Please Fill out the Fields.");
    dialog.setVisible(true);
    LogBttn.setEnabled(true);
    return; // stop here if empty
}   

String hashedPassword = Integer.toHexString(password.hashCode());
int userId = -1;

try {
    LogBttn.setEnabled(false);
    Connection conn = DatabaseConnection.connect();
   String sql = "SELECT u.user_id, u.password, u.role, m.reset_confirmed, m.is_banned, m.ban_reason " +
             "FROM user_table u " +
             "JOIN user_data m ON u.user_id = m.user_id " +
             "WHERE u.full_name = ?";

    PreparedStatement pst = conn.prepareStatement(sql);
    pst.setString(1, username);
    ResultSet rs = pst.executeQuery();

    if (rs.next()) {
        userId = rs.getInt("user_id");
        String role = rs.getString("role");
        Boolean isban = rs.getBoolean("is_banned");
        String ban_reason = rs.getString("ban_reason");
        String dbPassword = rs.getString("password");
        boolean passreset = rs.getBoolean("reset_confirmed");

        if(passreset) {
                 final int uid = userId;
                java.awt.EventQueue.invokeLater(() -> {
                   ConfirmedPass confirmedPassDialog = new ConfirmedPass(this, true, uid);
                    confirmedPassDialog.setVisible(true);
                    LogBttn.setEnabled(true);

                     if(confirmedPassDialog.isPasswordChanged()) {  // you can create a boolean property in ConfirmedPass
                        this.dispose();
                         new AdminA(uid).setVisible(true);
                      } 
                });     
        }
        
        else if (isban) {
            JOptionPane.showMessageDialog(null,"Your Account is banned.\nReason:" + ban_reason + 
                    "\n\n" + "If you believe this is a mistake, please contact the Administrator.","Access Denied"
                    ,JOptionPane.WARNING_MESSAGE);    
            LogBttn.setEnabled(true);
            return;
        }  
        
        
        else if (dbPassword.equals(hashedPassword)) { 
            if ("Admin".equalsIgnoreCase(role)||"S_Admin".equalsIgnoreCase(role)) {
            final int uid = userId;
            java.awt.EventQueue.invokeLater(() -> {
                new AdminA(uid).setVisible(true);
                this.dispose();
            });
            }
             else {
          
           JOptionPane.showMessageDialog(null, """
                                               Clients are no longer allowed to login after update v0.7c 
                                                Reason :
                                               The system has been converted from Management System.""","Access Denied"
            ,JOptionPane.WARNING_MESSAGE);    
           LogBttn.setEnabled(true);
            return;
        }
            
        }          
         else  { 
           Error error = new Error(this,true, "Wrong Username or Password");
           LogBttn.setEnabled(true);
           error.setVisible(true);     
           

        }
    } else {
        Error dialog = new Error(this, true, "User not Found.");
        dialog.setVisible(true);
        LogBttn.setEnabled(true);
    }

    rs.close();
    pst.close();
    conn.close();

    } catch (Exception e) {
        e.printStackTrace();
       
        Error dialog = new Error(this, true, "Error: " + e.getMessage());
        LogBttn.setEnabled(true);
        dialog.setVisible(true);
    }     // TODO add your handling code here:
    }//GEN-LAST:event_LogBttnActionPerformed

